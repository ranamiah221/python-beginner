# print even number 2 to 100 using for loops.
for i in range(2,101,2):
    print(i)

# print 1 to 100.
for i in range(1, 101):
    print(i)

# print 100 to 1
for i in range(100,0,-1):
    print(i)

# print the multiplication table of a number of n.
n= int(input("Enter the number : "))

for i in range(1,11):
    print(n*i)