marks =[6,1,2,3]

# add one element at the end
marks.append(5) 
print("append",marks) 

# sort in ascending order
marks.sort()
print("sort",marks)

# sorts  in descending order
marks.sort(reverse=True)
print("reverse sort",marks)

# reverse list
marks.reverse()
print("list reverse",marks)

# insert element at index
marks.insert(1,9)
print("set 1 index new value",marks)